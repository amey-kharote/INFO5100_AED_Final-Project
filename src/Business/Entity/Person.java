package Business.Entity;

/**
 *
 * @author Lavina
 */
public class Person {
    
    private String personName;
    private int personAge;
    private String personAddress;
    private String personPhoneNo;
    private String personEmailId;
    private String personBloodType;
    private String personSex;
    private String date;
    private String signature;
    private String network;

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }

    public int getPersonAge() {
        return personAge;
    }

    public void setPersonAge(int personAge) {
        this.personAge = personAge;
    }

    public String getPersonAddress() {
        return personAddress;
    }

    public void setPersonAddress(String personAddress) {
        this.personAddress = personAddress;
    }

    public String getPersonPhoneNo() {
        return personPhoneNo;
    }

    public void setPersonPhoneNo(String personPhoneNo) {
        this.personPhoneNo = personPhoneNo;
    }

    public String getPersonEmailId() {
        return personEmailId;
    }

    public void setPersonEmailId(String personEmailId) {
        this.personEmailId = personEmailId;
    }

    public String getPersonBloodType() {
        return personBloodType;
    }

    public void setPersonBloodType(String personBloodType) {
        this.personBloodType = personBloodType;
    }

    public String getPersonSex() {
        return personSex;
    }

    public void setPersonSex(String personSex) {
        this.personSex = personSex;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

    public String getNetwork() {
        return network;
    }

    public void setNetwork(String network) {
        this.network = network;
    }

}
